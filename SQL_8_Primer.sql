SELECT 'Пример 3'
SELECT 
    Название, 
    Столица, 
    Площадь, 
    Население, 
    Континент
FROM (
    SELECT 
        Название, 
        Столица, 
        Площадь, 
        Население, 
        Континент
    FROM Страны
    WHERE Континент = 'Африка'
) AS Африка
WHERE Население > 50000000;

SELECT 'Пример 4'
SELECT 
    A.Название,
    A.Столица,
    A.Площадь,
    A.Население,
    A.Континент,
    ROUND( A.Население * 100.0 / (
             SELECT SUM(B.Население)
             FROM Страны B
             WHERE B.Континент = A.Континент
          ), 3) AS Процент
FROM Страны A
ORDER BY Процент DESC;

SELECT 'Пример 5'
SELECT 
    A.Название,
    A.Столица,
    A.Площадь,
    A.Население,
    A.Континент
FROM Страны A
WHERE A.Население > (
    SELECT AVG(B.Население)
    FROM Страны B
    WHERE B.Континент = A.Континент
);

SELECT 'Пример 6'
SELECT *
FROM Страны
WHERE Континент IN (
    SELECT Континент
    FROM Страны
    GROUP BY Континент
    HAVING AVG(Население) > (SELECT AVG(Население) FROM Страны)
);

SELECT 'Пример 7'
SELECT 
    Название, 
    Столица, 
    Площадь, 
    Население, 
    Континент
FROM Страны
WHERE Континент = 'Азия'
  AND Население > ALL (
      SELECT Население
      FROM Страны
      WHERE Континент = 'Европа'
  );

