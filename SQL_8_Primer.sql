SELECT 'Пример 9'
SELECT 
    Название, 
    Столица, 
    Площадь, 
    Население, 
    Континент
FROM Страны
WHERE Континент = 'Африка'
  AND EXISTS (
      SELECT 1
      FROM Страны
      WHERE Континент = 'Африка'
        AND Население > 100000000
  );

